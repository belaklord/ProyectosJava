package bloque2;



public class booleano {
public static void main(String[]args){
	
	boolean resultado; 									// solo puede tener dos valores true o false//
	
	resultado = false;							// dependiendo de si le damos el valor true o el valor false//
	
	if (resultado){
		
		System.out.println("es verdadero");				// "!" significa lo contrario //
	}
		else {
			System.out.println("es falso");
		}
	
	if (!resultado){
		System.out.println("falso!");

	}
	}

}

